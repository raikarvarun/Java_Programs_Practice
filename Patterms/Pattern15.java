class Pattern15{
    public static void main(String []arg){

        int n = 9  ;
        for(int i = 1 ; i <=  n ; i++){
            for(int j = 1 ; j <= n ; j++){
                System.out.print("*  ");
            }
            System.out.println();
        }
        
    }
}